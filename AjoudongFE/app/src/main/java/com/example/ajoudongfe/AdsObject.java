package com.example.ajoudongfe;

public class AdsObject {
    private int adsID;
    private String advertiserID;
    private int adsView;
    private int adsSpace;
    private String adsIMG;

    public int getAdsID() {
        return adsID;
    }

    public void setAdsID(int adsID) {
        this.adsID = adsID;
    }

    public String getAdvertiserID() {
        return advertiserID;
    }

    public void setAdvertiserID(String advertiserID) {
        this.advertiserID = advertiserID;
    }

    public int getAdsView() {
        return adsView;
    }

    public void setAdsView(int adsView) {
        this.adsView = adsView;
    }

    public int getAdsSpace() {
        return adsSpace;
    }

    public void setAdsSpace(int adsSpace) {
        this.adsSpace = adsSpace;
    }

    public String getAdsIMG() {
        return adsIMG;
    }

    public void setAdsIMG(String adsIMG) {
        this.adsIMG = adsIMG;
    }
}
